var sensordata = {};

const sensors = {
  deviceorientation: {
    keys: ["alpha", "beta", "gamma"],
    listener: function (/** @type {DeviceOrientationEvent} */ evt) {
      if (evt.alpha === null) return;
      record(evt.type, {
        alpha: evt.alpha,
        beta: evt.beta,
        gamma: evt.gamma
      });
    }
  }
};

async function start_recording() {
  for (const [sensor, fun] of Object.entries(sensors)) {
    window.addEventListener(sensor, fun.listener, true);
    window.setInterval(classify, 1000);
    sensordata[sensor] = [];
  }
}

async function stop_recording() {
  for (const [sensor, fun] of Object.entries(sensors)) {
    window.removeEventListener(sensor, fun.listener, true);
    window.clearInterval();
  }
}

function record(eventtype, fields) {
  // time at which the event happend
  sensordata[eventtype].push(fields);
}

function classify() {
  var eventtype = "deviceorientation";
  var a = {
    alpha: [],
    beta: [],
    gamma: []
  };

  for (let entry in sensordata[eventtype]) {
    for (const [key, value] of Object.entries(sensordata[eventtype][entry])) {
      a[key].push(value);
    }
  }

  var input = [];
  for (const [key, value] of Object.entries(a)) {
    if (value.length === 0) {
      return;
    }

    input.push(math.mean(value));
    input.push(math.variance(value));
    input.push(math.min(value));
    input.push(math.max(value));
  }
  // get audio player by id
  const audioPlayer = document.getElementById("myAudio");
  let x = 0;
  // disply current value
  const valueDisplay = document.getElementById("valueDisplay");

  valueDisplay.textContent = score(input)[0];

  x = score(input)[0];

  // check whether music is paused or played
  if (x < 0.5 && !audioPlayer.paused) {
    audioPlayer.pause();
  }

  sensordata[eventtype] = [];
}

